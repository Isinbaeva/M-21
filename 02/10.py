a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
string = input("Введите строку: ")
if b == 0 and string == "/":
    print('888888')
elif string == "+":
    print(a + b)
elif string == "-":
    print(a - b)
elif string == "/":
    print(a / b)
elif string == "*":
    print(a * b)
else:
    print('888888')